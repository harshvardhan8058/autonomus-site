"""Tests for the plan Executor (`app.agent.executor`).

Covers:

- Task 13.2 / Property 9: every executed step emits a well-formed lifecycle. For
  any generated plan executed with arbitrary per-step tool success/failure, each
  executed step emits a ``step_started`` event followed by exactly one terminal
  event (``step_completed`` XOR ``step_failed``); its final status is ``done`` or
  ``failed`` accordingly; and a failed step never halts execution of steps whose
  dependencies are all ``done`` (Req 3.1, 3.3, 3.4).
- Task 13.3 (unit): best-effort degradation — when a bookkeeping dependency (the
  event bus's ``publish`` or the structured logger's ``decision``) raises, the
  run does not crash and every executed step still receives a terminal status
  (Req 3.5).

The Property 9 test drives a **scripted fake tool registry** whose per-tool
success/failure is generated by Hypothesis, and inspects the events buffered on
the :class:`~app.models.schemas.RunState` replay buffer (populated by the real
:class:`~app.core.event_bus.EventBus`). Property tests use Hypothesis at a
minimum of 100 iterations. Each generated example is driven through
:func:`asyncio.run` because :meth:`Executor.run` is asynchronous.
"""

from __future__ import annotations

import asyncio
from typing import Any

import pytest
from hypothesis import given, settings
from hypothesis import strategies as st

from app.agent.executor import Executor
from app.agent.tools import ToolRegistry, ToolResult
from app.core.event_bus import EventBus
from app.models.schemas import (
    Plan,
    PlanStep,
    RunState,
    SSEEventType,
    StepStatus,
)

# ---------------------------------------------------------------------------
# Scripted fake tool registry
# ---------------------------------------------------------------------------


class ScriptedToolRegistry:
    """A fake tool registry whose per-tool outcome is scripted (Req 3.4).

    Each tool name maps to a boolean: ``True`` means a dispatch succeeds and
    returns a :class:`ToolResult`; ``False`` means every dispatch raises. This
    lets a test drive arbitrary per-step success/failure while structurally
    satisfying the interface the :class:`Executor` uses (``names`` + ``dispatch``).

    Attributes:
        outcomes: Mapping of tool name to whether dispatching it succeeds.
        dispatched: Ordered list of the tool names dispatched (for assertions).
    """

    def __init__(self, outcomes: dict[str, bool]) -> None:
        self.outcomes = outcomes
        self.dispatched: list[str] = []

    def names(self) -> list[str]:
        """Return the registered tool names."""

        return list(self.outcomes)

    async def dispatch(self, name: str, **kwargs: Any) -> ToolResult:
        """Return a canned result for a succeeding tool, else raise (Req 3.4)."""

        self.dispatched.append(name)
        if self.outcomes.get(name, False):
            return ToolResult(output=f"output-{name}", summary=f"did {name}")
        raise RuntimeError(f"scripted failure for tool {name!r}")


# ---------------------------------------------------------------------------
# Hypothesis strategies
# ---------------------------------------------------------------------------

_run_ids = st.text(
    alphabet="abcdefghijklmnopqrstuvwxyz0123456789-",
    min_size=1,
    max_size=10,
)


@st.composite
def _scripted_plans(draw: st.DrawFn) -> tuple[Plan, dict[str, bool]]:
    """Draw a schema-valid plan plus a per-step tool success map.

    Each step ``i`` (1-based) is given the task name ``tool_{i}`` (so the
    executor routes it directly to that registered tool) and a random subset of
    strictly-earlier steps as its dependencies. The returned outcome map keys the
    per-step tool name to whether its dispatch should succeed.
    """

    n = draw(st.integers(min_value=2, max_value=5))
    steps: list[PlanStep] = []
    outcomes: dict[str, bool] = {}
    for i in range(1, n + 1):
        if i > 1:
            depends_on = draw(
                st.lists(
                    st.integers(min_value=1, max_value=i - 1),
                    unique=True,
                    max_size=i - 1,
                )
            )
        else:
            depends_on = []
        tool_name = f"tool_{i}"
        outcomes[tool_name] = draw(st.booleans())
        steps.append(
            PlanStep(
                step=i,
                task=tool_name,
                description=f"description for step {i}",
                expected_output=f"output {i}",
                depends_on=sorted(depends_on),
            )
        )
    return Plan(steps=steps), outcomes


def _expected_execution(
    plan: Plan, outcomes: dict[str, bool]
) -> tuple[set[int], set[int]]:
    """Compute the expected executed/done step sets for a scripted run.

    Mirrors the executor's single ascending pass: a step is *executed* when every
    dependency ended ``done``; an executed step ends ``done`` when its scripted
    tool succeeds, otherwise ``failed``.

    Returns:
        A ``(executed, done)`` tuple of step numbers.
    """

    done: set[int] = set()
    executed: set[int] = set()
    for step in sorted(plan.steps, key=lambda s: s.step):
        if all(dep in done for dep in step.depends_on):
            executed.add(step.step)
            if outcomes[f"tool_{step.step}"]:
                done.add(step.step)
    return executed, done


def _new_run_state(run_id: str, plan: Plan) -> RunState:
    """Build a :class:`RunState` carrying ``plan`` for execution."""

    state = RunState(run_id=run_id, request="do the thing", client_ip="127.0.0.1")
    state.plan = plan
    return state


