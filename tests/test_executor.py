# Feature: autonomous-agent-service, Property 9: Every executed step emits a well-formed lifecycle
@pytest.mark.property
@settings(max_examples=100, deadline=None)
@given(run_id=_run_ids, data=_scripted_plans())
def test_property_9_step_lifecycle_and_failure_continuation(
    run_id: str, data: tuple[Plan, dict[str, bool]]
) -> None:
    """Property 9: each executed step emits a well-formed lifecycle.

    **Validates: Requirements 3.1, 3.3, 3.4**

    For any generated plan executed with arbitrary per-step tool success/failure:
    each executed step emits a ``step_started`` event followed by exactly one
    terminal event (``step_completed`` XOR ``step_failed``); its final status is
    ``done`` or ``failed`` accordingly; steps whose dependencies never complete
    are left ``pending``; and a failed step never halts execution of steps whose
    dependencies are all ``done``.
    """

    plan, outcomes = data
    expected_executed, expected_done = _expected_execution(plan, outcomes)

    async def _run() -> None:
        registry = ScriptedToolRegistry(outcomes)
        bus = EventBus()
        state = _new_run_state(run_id, plan)
        # ``max_retries=0`` keeps the property fast; retry bounds are covered
        # elsewhere. Lifecycle/continuation behavior is independent of the count.
        executor = Executor(registry, bus, max_retries=0)  # type: ignore[arg-type]

        await executor.run(state)

        events = state.events
        started_steps = [e.step for e in events if e.type is SSEEventType.STEP_STARTED]
        completed_steps = [
            e.step for e in events if e.type is SSEEventType.STEP_COMPLETED
        ]
        failed_steps = [e.step for e in events if e.type is SSEEventType.STEP_FAILED]

        status_by_number = {s.step: s.status for s in plan.steps}

        for step in plan.steps:
            n = step.step
            started_for_n = started_steps.count(n)
            completed_for_n = completed_steps.count(n)
            failed_for_n = failed_steps.count(n)

            if n not in expected_executed:
                # Unsatisfiable dependencies: never executed, left pending.
                assert status_by_number[n] is StepStatus.PENDING
                assert started_for_n == 0
                assert completed_for_n == 0
                assert failed_for_n == 0
                continue

            # Executed: exactly one start and exactly one terminal event.
            assert started_for_n == 1
            assert completed_for_n + failed_for_n == 1

            # step_started precedes its terminal event.
            start_index = next(
                i
                for i, e in enumerate(events)
                if e.type is SSEEventType.STEP_STARTED and e.step == n
            )
            terminal_index = next(
                i
                for i, e in enumerate(events)
                if e.type
                in (SSEEventType.STEP_COMPLETED, SSEEventType.STEP_FAILED)
                and e.step == n
            )
            assert start_index < terminal_index

            if n in expected_done:
                assert status_by_number[n] is StepStatus.DONE
                assert completed_for_n == 1
                assert failed_for_n == 0
            else:
                assert status_by_number[n] is StepStatus.FAILED
                assert failed_for_n == 1
                assert completed_for_n == 0

        # A failed step never halts execution: every dependency-satisfied step is
        # executed even when an earlier independent step failed.
        for step in plan.steps:
            if all(dep in expected_done for dep in step.depends_on):
                assert step.step in expected_executed

    asyncio.run(_run())


# ---------------------------------------------------------------------------
# Task 13.3 unit tests — best-effort degradation (Req 3.5)
# ---------------------------------------------------------------------------


def _success_registry() -> ToolRegistry:
    """Build a real registry whose two tools always succeed."""

    registry = ToolRegistry()

    async def ok_tool(**kwargs: Any) -> ToolResult:
        return ToolResult(output="ok", summary="ok")

    registry.register("tool_1", ok_tool)
    registry.register("tool_2", ok_tool)
    return registry


def _two_step_plan() -> Plan:
    """Build a simple two-step plan routed to always-succeeding tools."""

    return Plan(
        steps=[
            PlanStep(step=1, task="tool_1", description="first", expected_output="a"),
            PlanStep(step=2, task="tool_2", description="second", expected_output="b"),
        ]
    )


class _BrokenBus:
    """An event bus whose ``publish`` always raises (bookkeeping failure)."""

    async def publish(self, event: Any, run_state: Any = None) -> None:
        raise RuntimeError("event bus is down")


class _BrokenLogger:
    """A logger whose ``decision`` always raises (bookkeeping failure)."""

    def decision(self, *args: Any, **kwargs: Any) -> None:
        raise RuntimeError("logger is down")


async def test_run_does_not_crash_when_event_bus_publish_raises() -> None:
    """A failing event bus never crashes the run; steps still terminalize (Req 3.5)."""

    plan = _two_step_plan()
    state = _new_run_state("run-bus", plan)
    executor = Executor(_success_registry(), _BrokenBus())  # type: ignore[arg-type]

    # Must not raise despite every publish failing.
    await executor.run(state)

    assert all(step.status is StepStatus.DONE for step in plan.steps)


async def test_run_does_not_crash_when_logger_decision_raises() -> None:
    """A failing logger never crashes the run; steps still terminalize (Req 3.5)."""

    plan = _two_step_plan()
    state = _new_run_state("run-log", plan)
    executor = Executor(_success_registry(), EventBus(), _BrokenLogger())  # type: ignore[arg-type]

    await executor.run(state)

    assert all(step.status is StepStatus.DONE for step in plan.steps)


async def test_failed_step_still_terminalizes_under_broken_bookkeeping() -> None:
    """Even with broken bookkeeping, a failing tool yields a FAILED status (Req 3.5)."""

    registry = ToolRegistry()

    async def boom(**kwargs: Any) -> ToolResult:
        raise RuntimeError("tool exploded")

    registry.register("tool_1", boom)
    registry.register("tool_2", boom)

    plan = _two_step_plan()
    state = _new_run_state("run-fail", plan)
    # Broken bus + zero retries: the run must still complete with terminal states.
    executor = Executor(registry, _BrokenBus(), _BrokenLogger(), max_retries=0)  # type: ignore[arg-type]

    await executor.run(state)

    assert all(step.status is StepStatus.FAILED for step in plan.steps)
    assert all(step.error for step in plan.steps)
